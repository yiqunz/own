package com.iclazz.something.common;

/**
 * list node
 * singly linked list
 *
 * @author yiqunz
 * @date 2022-08-19 09:49
 **/
public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
        super();
    }

    public ListNode(int val) {
        super();
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        super();
        this.val = val;
        this.next = next;
    }




}
